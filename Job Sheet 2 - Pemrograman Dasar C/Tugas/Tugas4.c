#include<stdio.h>

int main(){
	// Deklarasi variabel panjang dan lebar dalam cm
	float panjang = 10.0;
	float lebar = 5.0;
	
	// Menghitung luas persegi panjang
	float luas = panjang*lebar;
	
	// Menampilkan hasil
	printf("Panjang: %.2f cm\n", panjang);
	printf("Lebar: %.2f cm\n", lebar);
	printf("Luas persegi panjang: %.2f cm^2\n", luas);
	
	return 0;
}
